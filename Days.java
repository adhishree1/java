class Days
{
	public static void main(String[] args)
	{
	    String str="sun";
	        switch(str)
	        {
                case "sun":
                  System.out.println("Sunday");
                  break;
                case "mon":
                  System.out.println("Monday");
                  break;
                case "tue":                   
                  System.out.println("Tuesday");
                  break;
                default:  
                  System.out.println("Wrong choice");  
	       }
	}
}